# Python3 code to demonstrate working of 
# Adding Tuple to List and vice - versa
# Using += operator (list + tuple)
 
# initializing list
list = [5, 6, 7]
 
# printing original list
print("The original list is : " + str(list))
 
# initializing tuple 
tup = (9, 10)
 
# Adding Tuple to List and vice - versa
# Using += operator (list + tuple)
list += tup
 
# printing result 
print("The list after addition : " + str(list))